int		ft_srch_arrelem(int *arr, int size, int elem)
{
	int		i;

	i = 0;
	if (arr && elem >= 0)
	{
		while (i < size)
		{
			if (arr[i] == elem)
				return (i);
			i++;
		}
	}
	return (-1);
}
